  'N2': 0.0,
};


double stoichOxygen(Fuel f) {
  return f.C + f.H / 4.0 - f.O / 2.0;
}

// Calculate H/RT

double reactantEnthalpy({
  required Fuel fuel,
  required double airO2,
  required double airN2,
  required double airAR,
  required double airCO2,
  required double n_vapor,
  double Tfuel = 298.15,
  double Tair = 298.15,
}) {

  double h = fuel.get_hf(Tfuel);
  h += airO2 * (enthalpyNASA(o2, Tair) );
  h += airN2 * (enthalpyNASA(n2, Tair) );
  h += airAR *(enthalpyNASA(Ar, Tair));
  h += airCO2 *(enthalpyNASA(co2, Tair));  
  h += n_vapor*(enthalpyNASA(h2o, Tair));
  return h;
}


//   Gibbs Solver with pressure
Map<Species, double> gibbsMinimize({
  required List<Species> species,
  required Map<String, double> elementTotals,
  required double Q,
  required bool soot,
  required bool liquid,
  required List<double> B,
  required List<List<double>> A,
  double P = 1.0,   // atm
  double T = 1000.0,
  int maxIter = 10000,
}) {
  final elements = elementTotals.keys.toList();
  final NS = species.length - 2;  
  final NE = elements.length;
  final NTOT = NS + 2 + NE;
  const R = 8.314462618;
  bool gas_done = false;
  bool condensed_seen = false;
  // Initial guesses
  final n = List.filled(NTOT, 1e-10);
  final mat = List.generate(NTOT, (_) => List.filled(NTOT, 0.0));
  final rhs = List.filled(NTOT, 0.0);
  final g0_RT = List.filled(NS+2, 0.0);
// Initialize remainder of species 17 to 21
for (int i = 17; i < 22; i++ ){
  n[i]=-10.0;
}
// ***********************************************************
bool loop = false;
double sumN = 0.0;
for(int outer = 0; outer < 10000; outer++ ){  
  
  loop = false;
  for (int iter = 0; iter < maxIter; iter++) {
    //final sumN = n.reduce((a, b) => a + b);
    sumN = 0.0;
    for(int i = 0; i < NS; i++){
      sumN += n[i];
    }
    // Zero matrices
    for (int i = 0; i < NTOT; i++) {
      rhs[i] = 0.0;
      for (int j = 0; j < NTOT; j++) {
        mat[i][j] = 0.0;
      }
    }

    // Species equations
    int I = 0;
    if(T >= 273.15 && T < 600) I = 1;
    for (int j = 0; j < NS + 1 + I; j++) {
      g0_RT[j] = gibbsNASA(species[j].thermo, T) / (R * T);
    }
    int i_Liq = 0;
    if(liquid && T < 600){     // g0_RT for condensed water
      A[1][16] = 2.0;
      A[2][16] = 1.0;
      i_Liq = 1;
      int j = 16;
      g0_RT[j] = gibbsNASA(species[j].thermo, T) / (R * T);  
    }else{
      A[1][16] = 0.0;
      A[2][16] = 0.0;      
    }
    
    if(soot){
      A[0][15] = 1.0;
    }else{
      A[0][15] = 0.0;
    }

    for (int j = 0; j < NS+1+i_Liq; j++) {  
      if(j < NS) mat[j][j] = 1.0 / n[j];
      double s = 0.0;
      for (int i = 0; i < NE; i++){
        s += A[i][j]*n[NS + 2 + i];
      }
      if(j <NS){
        rhs[j] = -(g0_RT[j] + log(n[j]) - log(sumN) + log(P) + s);
      } else {
        rhs[j] = -(g0_RT[j] + s); 
      }

      for (int i = 0; i < NE; i++) {
        mat[j][NS + 2 + i] = A[i][j];
        mat[NS + 2 + i][j] = A[i][j];
      }
    }
    // Element constraints
    for (int i = 0; i < NE; i++) {
      double s = 0.0;
      for (int j = 0; j < NS+2; j++) {
        s += n[j] * A[i][j];
      }
      rhs[NS + 2 + i] = B[i] - s;
    }

// if soot is active
    if(soot == true){
      mat[NS][NS]   = 0.0;
    }else{
      mat[NS][NS] = 1.0;
      rhs[NS] = 0.0;
    }
// end of soot active 
    if(liquid){
      mat[16][16] = 0.0;
    }else{
      mat[16][16] = 1.0;
      rhs[16] = 0.0;
    }

    // Solve linear system
    gaussSolve(mat, rhs);

    double error = 0.0;
    for (int j = 0; j < NTOT; j++) {
      if(outer < 2 ){
        n[j] += 0.04*rhs[j];
      }else{
        n[j] += 0.9* rhs[j];
      }
      if (j != NS && j != NS+1 && n[j] < 0.0) n[j] = 1e-25;
     
     error += rhs[j].abs();
    }
    if(error < 1.0e-12) break; 
  }   

  gas_done = true;
// ************************ END OF INNER LOOP ***************************
  if(soot && n[NS]<0.0){
    soot = false;
    n[NS] = 1.0e-12;
    continue;    
  }
  if(liquid && n[NS+1] < 0.0){
    liquid = false;
    n[NS+1] = 1.0e-12;
    continue;
  }
//  check soot formation
    if(soot == false && gas_done){
      double g_limit = gibbsNASA(species[15].thermo, T) / (R * T); 
      double fi = g_limit + n[17];
      if( fi < 0.0){        //< -1.0e-10
        soot = true;
        n[15] = 1.0e-10;
        loop = true;
      }
    }

    if(T > 273.15 && T < 600.0  && liquid == false && gas_done ){
      double ph2o = g0_RT[6] + log(n[6]) - log(sumN) + log(P);
      g0_RT[16] = gibbsNASA(species[16].thermo, T) / (R * T);
      double ph2oL = g0_RT[16];
      if(ph2oL < ph2o ){
        liquid = true;
        n[16] = 1.0e-6;
        loop = true;
      }
    }

  if(loop == true) continue; // skip temp calculation (cycle)
  condensed_seen = true;

// Calculate  Temperature if T_calc is true
  if(GlobalData.T_calc == false){
    gas_done = true;
    if(condensed_seen){
     break;
     }else{
      continue;
     }
  }

  double Hprod = 0.0;
  double CpMix = 0.0;
  int I = 0;
  if(soot== true) I =1;
  int Is = 0;
  if(liquid) Is = 1;
  for(int j = 0; j < NS+I+Is; j++) {
    Hprod += n[j] * enthalpyNASA(species[j].thermo, T);
    CpMix += n[j] * cpNASA(species[j].thermo, T); // you already have Cp via NASA
  }
  final Hreact = Q; 
  final err = Hreact - Hprod;
  final dT = 0.4*err / CpMix;
  T += dT;
  if (dT.abs() < 1e-10 && err.abs()<1.0e-10) break;
}
//  print('Flame Temperatur $T' );
GlobalData.Tad = T;
GlobalData.soot1 = soot;
GlobalData.liquid1 = liquid;

 return {for (int i = 0; i < NS+2; i++) species[i]: n[i]};
}

//=====================GAUS SOLVER =========================
void gaussSolve(List<List<double>> A, List<double> b) {
  final n = b.length;

  for (int i = 0; i < n; i++) {
    int maxRow = i;
    for (int k = i + 1; k < n; k++) {
      if (A[k][i].abs() > A[maxRow][i].abs()) maxRow = k;
    }

    final tmpA = A[i];
    A[i] = A[maxRow];
    A[maxRow] = tmpA;

    final tmpB = b[i];
    b[i] = b[maxRow];
    b[maxRow] = tmpB;

    for (int k = i + 1; k < n; k++) {
      final f = A[k][i] / A[i][i];
      for (int j = i; j < n; j++) {
        A[k][j] -= f * A[i][j];
      }
      b[k] -= f * b[i];
    }
  }

  for (int i = n - 1; i >= 0; i--) {
    double sum = b[i];
    for (int j = i + 1; j < n; j++) {
      sum -= A[i][j] * b[j];
    }
    b[i] = sum / A[i][i];
  }
}



