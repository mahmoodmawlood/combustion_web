double h_water(double t) {
  double t1 = 273.15;
  double t2 = 373.15;
  double t3 = 600.0;
  double R = 8.314462618;
  List<double> a = List.filled(9, 0.0);
  if(t >= t1 && t <= t2){
    a = [1.326371304E+09,-2.448295388E+07, 1.879428776E+05,-7.678995050E+02,
    1.761556813E+00,-2.151167128E-03, 1.092570813E-06, 1.101760476E+08,-9.779700970E+05];
  }else if(t >= t2 && t <= t3){
    a = [1.263631001E+09,-1.680380249E+07, 9.278234790E+04,-2.722373950E+02,
    4.479243760E-01,-3.919397430E-04, 1.425743266E-07, 8.113176880E+07,-5.134418080E+05];
  }

double h = -a[0]/(t*t)
      + a[1] * log(t) /t + a[2]
      + a[3] * t /2.0
      + a[4] * t * t  / 3.0
      + a[5] * t * t * t  / 4.0
      + a[6] * t*t*t*t/5.0 + a[7]/t;

  return h * R * t; // J/mol  
}
