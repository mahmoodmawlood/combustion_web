class NASAPolynomial {
  final double Tmin;
  final double Tmax;
  final List<double> a; // a1..a7

  const NASAPolynomial(this.Tmin, this.Tmax, this.a);
}

// class SpeciesThermo
class SpeciesThermo {
  final String name;
  final NASAPolynomial low;
  final NASAPolynomial high;
  final NASAPolynomial highh;
  const SpeciesThermo(this.name, this.low, this.high, this.highh);

NASAPolynomial coeff(double T) {
  if (T >= low.Tmin && T <= low.Tmax) {
    return low;
  } else if (T >= high.Tmin && T <= high.Tmax) {
    return high;
  } else if (T >= highh.Tmin ) {
    return highh;
  } else {
    throw Exception("Temperature $T is out of valid range for $name");
  }
}
}

double enthalpyNASA(SpeciesThermo sp, double t) {
  final a = sp.coeff(t).a;
  const R = 8.314462618;  
  double h_RT = -a[0]/(t*t)
      + a[1] * log(t) /t + a[2]
      + a[3] * t /2.0
      + a[4] * t * t  / 3.0
      + a[5] * t * t * t  / 4.0
      + a[6] * t*t*t*t/5.0 + a[7]/t;

  return h_RT * R * t; // J/mol

}

// Entropy function
double entropyNASA(SpeciesThermo sp, double T) {
  final a = sp.coeff(T).a;
  const R = 8.314462618;
//-C(1)/(2.0D0*T*T) - C(2)/T + C(3)*LOG(T) + C(4)*T + C(5)*T*T/2.0D0 &
//       +C(6)*T**3/3.0D0 + C(7)*T**4/4.0D0 + C(9)
 return R *
      (-a[0]/(2.0*T*T) -
       a[1] / T +
       a[2] * log(T) +
       a[3] * T +
       a[4] * T * T / 2.0 +
       a[5]*T*T*T/3.0 + 
       a[6]*T*T*T*T/4.0 +
       a[8] );
}

double cpNASA(SpeciesThermo sp, double T){
  final a = sp.coeff(T).a;
  const R = 8.314462618;
//            C(1)/(T*T) + C(2)/T + C(3) + C(4)*T + C(5)*T*T + C(6)*T**3 + C(7)*T**4
  return R *
    (
    a[0]/(T*T) +
    a[1] / T +
    a[2] +
    a[3] * T +
    a[4] * T * T +
    a[5] * T * T * T +
    a[6] *T*T*T*T
  );
}

// Gibbs free energy
double gibbsNASA(SpeciesThermo sp, double T) {
  final h = enthalpyNASA(sp, T);
  final s = entropyNASA(sp, T);
  return h - s*T;
}

