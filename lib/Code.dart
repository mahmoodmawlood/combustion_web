class Fuel {
  final String name;
  final double C, H, O, N;
  final double hf, t1, t2, t3;
  final List<double> low;
  final List<double> high;
  Fuel({this.name=' ', this.C=0,  this.H=0, this.O=0,this.N=0, 
    this.hf=0, required this.t1, required this.t2, required this.t3,
    required this.low, required this.high});

  double get_hf(double t) {
  const double R = 8.314462618; // J/mol-K

  // 1. No polynomial data → return hf directly
  if (low.isEmpty && high.isEmpty) {
    return hf;
  }

  List<double> a;

  // 2. Only one range
  if (high.isEmpty && t2 > 0.0) {
    a = low;
  } 
  // 3. Two ranges
  else {
    if (t < t2) {
      a = low;
    } else {
      a = high;
    }
  }

  // 4. NASA enthalpy equation
  double h_RT = -a[0]/(t*t)
      + a[1] * log(t) /t + a[2]
      + a[3] * t /2.0
      + a[4] * t * t  / 3.0
      + a[5] * t * t * t  / 4.0
      + a[6] * t*t*t*t/5.0 + a[7]/t;

  return h_RT * R * t; // J/mol
}}

