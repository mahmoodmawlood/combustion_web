class Visc {
  final String name;
  final double mw, t1, t2, t3, t4;
  final List<double> low;
  final List<double> high;
  final List<double> highh;
  Visc({this.name=' ', this.mw = 0, required this.t1, required this.t2, required this.t3,
    required this.t4, required this.low, required this.high, required this.highh});

  double get_visc(double t) {
  
  List<double> a=[];
  if(t < t1 ){
    a = low;
  }
  if (t >= t1 && t<= t2) {
    a = low;
  } 
  if (t >=t2 && t<= t3) {
      a = high;
  }
  if (t4>0.0 && t>t3){
    a = highh;
  } 
  // calculate eta - viscosity of each gas component
  double eta = a[0]*log(t) 
    + a[1]/t + a[2]/(t*t)
    + a[3];
  return exp(eta); 
}}

