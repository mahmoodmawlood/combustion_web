List<List<double>> phii(int k, List<double>mw, int NS, List<double> eta){
   final  fii = List.generate(NS, (_) => List.filled(NS, 0.0));
   final  psi = List.generate(NS, (_) => List.filled(NS, 0.0));
    for(int i = 0; i < NS; i++){
        for(int j = 0; j < NS; j++){
            double t1 = 1.0 + sqrt((eta[i]/eta[j]))* pow( mw[j]/mw[i],0.25 );
            double t2 = sqrt(2.0*mw[j]/(mw[i]+mw[j]));
            fii[i][j]=0.25*t1*t1*t2;
        }
    }
    
    for (int i = 0; i < NS; i++){
        for (int j = 0; j < NS; j++){
            double t1 = 2.4*(mw[i]-mw[j])* (mw[i] - 0.142*mw[j]);
            double t2 = 1.0*pow((mw[i]+mw[j]), 2.0);
            psi[i][j] = fii[i][j]*(1.0 + t1/t2);
        }
    }
    
    if(k==1) {
        return fii;
    }else{
        return psi;
    }
    //if(k==2) return psi;
}